package MasterClass;

public class Concatination {
	
	public static void main(String[] args) {
		
		int count = 10;
		int extracount = 20;
		System.out.println("Extra count is ::" + (count + extracount));
		
		/*
		 * Integer + Integer = Integer String + String = String String + Integer =
		 * String
		 */
	}
	

}
