package String;

public class ToUppercase {
   
	public static void main(String[] args) {
		String str = "Hello";
		
		//Convert to Uppercase
		String s1 = str.toUpperCase();
		System.out.println(s1);
		
		//Convert to Lower case
		String s2 = str.toLowerCase();
		System.out.println(s2);
		
		
		
	}
}
